x = 2
# assignment evaluates to the RHS, so this expression
# evaluates to 2
#

puts x = 2
# puts always evaluates to nil
#

p name = "Joe"
# assignment evaluates to the RHS, so name = "Joe" 
# evaluates to "Joe", and p evaluates to the object 
# passed to it, which is "Joe"
#

four = "four"
# assignment evaluates to the RHS, so this returns "four"
#

print something = "nothing"
# the assignment evaluates to "nothing", and print always 
# evalutes to nil, so this expresion evaluates to nil
#




